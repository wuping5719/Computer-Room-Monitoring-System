function sAlert(str){
	var msgw,msgh,bordercolor;
	msgw=400;//提示窗口的宽度
	msgh=100;//提示窗口的高度
	titleheight=25 //提示窗口标题高度
	bordercolor="#086ebf";//提示窗口的边框颜色
	titlecolor="#086ebf";//提示窗口的标题颜色

	var sWidth,sHeight;
	sWidth=screen.width;
	sHeight=screen.height;

	var bgObj=document.createElement("div");
	bgObj.setAttribute('id','bgDiv');
	bgObj.style.position="absolute";
	bgObj.style.top="0";
	bgObj.style.background="#000000";
	bgObj.style.filter="progid:DXImageTransform.Microsoft.Alpha(style=3,opacity=25,finishOpacity=75";
	bgObj.style.opacity="0.2";
	bgObj.style.left="0";
	bgObj.style.width=1349 + "px";
	bgObj.style.height=900 + "px";
	bgObj.style.zIndex = "10000";
	document.body.appendChild(bgObj);

	var msgObj=document.createElement("div")
	msgObj.setAttribute("id","msgDiv");
	msgObj.setAttribute("align","center");
	msgObj.style.background="#fff";
	msgObj.style.border="1px solid " + bordercolor;
	msgObj.style.position = "fixed";
	msgObj.style.left = "480px";
	msgObj.style.top = "280px";
	msgObj.style.font="15px/1.6em Verdana, Geneva, Arial, Helvetica, sans-serif, Microsoft YaHei";
	msgObj.style.color="#000";
	msgObj.style.marginLeft = "0px" ;
	/*msgObj.style.marginTop = -75+document.documentElement.scrollTop+"px";*/
	msgObj.style.width = msgw + "px";
	msgObj.style.height =msgh + "px";
	msgObj.style.textAlign = "center";
	msgObj.style.lineHeight ="25px";
	msgObj.style.zIndex = "10001";

	   var title=document.createElement("h4");
	   title.setAttribute("id","msgTitle");
	   title.setAttribute("align","right");
	   title.style.margin="0";
	   title.style.padding="3px";
	   title.style.background=bordercolor;
	   title.style.filter="progid:DXImageTransform.Microsoft.Alpha(startX=20, startY=20, finishX=100, finishY=100,style=1,opacity=75,finishOpacity=100);";
	   title.style.opacity="0.75";
	   title.style.border="1px solid " + bordercolor;
	   title.style.height="18px";
	   title.style.font="12px Verdana, Geneva, Arial, Helvetica, sans-serif, Microsoft YaHei";
	   title.style.color="white";
	   title.style.cursor="pointer";
	   title.innerHTML="关闭";
	   title.onclick=function(){
				document.body.removeChild(bgObj);
				document.getElementById("msgDiv").removeChild(title);
				document.body.removeChild(msgObj);
	   }
	   document.body.appendChild(msgObj);
	   document.getElementById("msgDiv").appendChild(title);
	   var txt=document.createElement("p");
	   txt.style.margin="1em 0"
	   txt.setAttribute("id","msgTxt");
	   txt.innerHTML=str;
	   document.getElementById("msgDiv").appendChild(txt);
	}